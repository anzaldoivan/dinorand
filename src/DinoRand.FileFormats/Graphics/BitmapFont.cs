/// drawing the seed watermark into 15/16-bit title images
/// (docs/decisions/cross/SEED-WATERMARK-PLAN.md). Replaces BioRand's Windows-only
/// System.Drawing rasterization with a dependency-free blit.
///
/// <para>Lit pixels are written as white (<c>0x7FFF</c>) with <b>bit 15 of the overwritten
