    /// <summary>
    /// Draw <paramref name="text"/> into a little-endian 16bpp pixel buffer of
    /// <paramref name="width"/>×<paramref name="height"/>, pen at (<paramref name="x"/>,
    /// <paramref name="y"/>) = the glyph box's top-left. Out-of-bounds pixels are clipped
    /// (no wrap, no throw).
    /// </summary>
    public static void Blit(Span<byte> pixels16, int width, int height, int x, int y, string text)
    {
        int penX = x;
        foreach (var c in text)
        {
            if (Glyphs.TryGetValue(c, out var rows)
                || Glyphs.TryGetValue(char.ToUpperInvariant(c), out rows))
            {
                for (int row = 0; row < GlyphHeight; row++)
                {
                    int py = y + row;
                    if (py < 0 || py >= height) continue;
                    for (int col = 0; col < GlyphWidth; col++)
                    {
                        if ((rows[row] >> (GlyphWidth - 1 - col) & 1) == 0) continue;
                        int px = penX + col;
                        if (px < 0 || px >= width) continue;
                        var at = pixels16.Slice((py * width + px) * 2);
                        ushort old = BinaryPrimitives.ReadUInt16LittleEndian(at);
                        BinaryPrimitives.WriteUInt16LittleEndian(at, (ushort)(0x7FFF | (old & 0x8000)));
                    }
                }
            }
            penX += Advance;
        }
    }
